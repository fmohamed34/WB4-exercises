//Part1 is where we start e.g. i=0
//Part2 where do we stop e.g. 7
//Part3 how do we get there e.g. i++ AKA increment by one
//For - Part1; Part2; Part3;
for(let i=0; i < 7; i++){
console.log(i, "I LOVE YOU");
}

//while works similarly but the parts are on separate lines
let i = 1; //where to start
while (i <= 7) {   //where to stop
console.log("I LOVE YOU")
i++; //increment (go up by one like i=i+1)
}